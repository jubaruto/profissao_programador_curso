// ###### ARRAYS ######
// É uma lista repleta de valores.
// Sendo uma lista única é chamada de vetor. Sendo uma lista de outras listas é chamada de matriz.
// É utilizado colchetes para declarar uma lista.
// Cada item da lista tem posição específica, e no caso dos arrays, as posições começam exatamente pelo zero (0), denominada Index.

// Exemplo de lista de vetor
const vetor = ['Juliana', 'Julia', 'Lucas', 'Guinha', 'Clara', 'Mike', 13, 27, true, false, {}, null];

console.log(vetor);
console.log(vetor[0]);
console.log(vetor[1]);
console.log(vetor[6]);
console.log(vetor[7]);

